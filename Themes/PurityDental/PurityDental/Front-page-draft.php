<script>
let currentIndex = 0;
const reviewWidth = document.querySelector('.review').offsetWidth;
const reviewsContainer = document.querySelector('.reviews-container');
const reviews = document.querySelectorAll('.review');
const totalReviews = reviews.length;

function showNextReview() {
    currentIndex++;
    const offset = currentIndex * -reviewWidth;
    reviewsContainer.style.transition = 'transform 0.5s ease';
    reviewsContainer.style.transform = `translateX(${offset}px)`;

    // When reaching the 3rd last slide, jump back to the first slide
    if (currentIndex >= totalReviews - 2) {
        setTimeout(() => {
            currentIndex = 0;
            const offset = currentIndex * -reviewWidth;
            reviewsContainer.style.transition = 'none';
            reviewsContainer.style.transform = `translateX(${offset}px)`;
            setTimeout(() => {
                reviewsContainer.style.transition = 'transform 0.5s ease';
            }, 50);
        }); // Delay the transition for 500ms to allow the last reviews to be visible momentarily
    }
}

setInterval(showNextReview, 8000); // Slide every 8 seconds

</script>
